class LongestExactSum{
		public static void main(String args[]){
		int arr[]={4,1,1,1,2,3,5};
		int left=0;
		int maxLen=0;
		int sum=0;
		int k=5;
		for(int right=0;right<arr.length;right++){
			sum+=arr[right];
			while(sum>k){
				sum-=arr[left];
				left++;
			}
			if(sum==k){
				maxLen=Math.max(maxLen,right-left+1);
			}
		}
		System.out.println(maxLen);
	}
}
